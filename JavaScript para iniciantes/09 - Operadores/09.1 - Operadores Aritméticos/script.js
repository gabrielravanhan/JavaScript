// + => Adição
// - => Subtração
// * => Multiplicação
// / => Divisão
// ** => Exponenciação
// % => Sobra de uma divisão

let salario = 10;
console.log(salario + 2);
console.log(salario - 2);
console.log(salario * 2);
console.log(salario / 2);
console.log(salario ** 2);
console.log(salario % 2);

// ++ => Incremento
// -- => Decremento
console.log(++salario); // salario + 1 => salario = 11
console.log(--salario); // salario - 1 +> salario = 10