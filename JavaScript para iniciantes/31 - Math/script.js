// Função Math

// Chama um número aleatório entre 0 e 1
console.log(Math.random());

// Mostra o maior valor
console.log(Math.max(1, 2, 15, 10));

// Mostra o menor valor
console.log(Math.min(1, 2, 15, 10));

// Calcula potências
console.log(Math.pow(10, 3));

// Calcula raízes quadrada
console.log(Math.sqrt(9));